<script>
import getPost from "@/composeables/getPost";

export default {
  props: ["id"],
  setup(props) {
    const {post, error, load} = getPost(props.id)
    load()

    return {
      post, error
    }
  }
}
</script>

<template>
  <div v-if="error">{{error}}</div>
  <div v-if="post" class="post">
    <h3>{{post.title}}</h3>
    <p>{{post.body}}</p>
    <span v-for="tag in post.tags" :key="tag">#{{tag}}</span>
  </div>
</template>

